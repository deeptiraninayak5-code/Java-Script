// Comparison operator will always result in Boolean true or false.
// =, ==, ===

// = -> Assignment Operator 
// == -> Loose comparison (Sikh vs Hindu)
// === -> Strict Comparison (Sikh vs Hindu, Language, Living)

// <, >, <=, >=, !, !==

console.log(3 > 4) //false
console.log(3 < 4) //true
console.log(4 >= 4) //true
console.log(3 <= 4) //true
console.log(5 == "5") //true  //Loose Couple Comparison 
console.log(5 === "5") //false //Strict not allowed
console.log(5 != "5") //false, 5=int, "5"=string, Both of them are not equal, loose coupled. 
console.log(5 !== "5") //true 
// console.log(5 !=== "5") //This does not exist. 
//  >==, <== //This does not exist. 

// === Strict check, we will check for both the data type and value.
// == Loose check: we will check either value or data type. 



