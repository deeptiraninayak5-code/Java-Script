let score = 99

if (score >= 90){
    console.log("Grade A -Excellent.")
}else if (score >= 80){
    console.log("Grade B - Good .")
}else if (score >= 70){
    console.log("Grade C - Average .")
}else if (score >= 60){
    console.log("Grade A - Below average .")
}else{
    console.log("Grade F -Fail.")
}